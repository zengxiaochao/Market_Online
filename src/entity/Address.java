package entity;

public class Address {
    private String name;
    private String address;
    private String tel;
    private String address_all;
    private String address_who;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getAddress() {
        return address;
    }

    public void setAddress(String address) {
        this.address = address;
    }

    public String getTel() {
        return tel;
    }

    public void setTel(String tel) {
        this.tel = tel;
    }

    public String getAddress_all() {
        return address_all;
    }

    public void setAddress_all(String address_all) {
        this.address_all = address_all;
    }

    public String getAddress_who() {
        return address_who;
    }

    public void setAddress_who(String address_who) {
        this.address_who = address_who;
    }
}
